                        <h3>Preparing for interviews</h3>
                        <p>
                            If you are invited for a job interview: Congratulations! You already made it very far! Now you must prove that you are
                            the most suitable candidate for the position.
                            <br>
                            <br>
                            I have some tips for you:<br>
                            A job interview is a meeting with an objective. It is the employer’s objective to find the best person for the job.
                            Therefore, an employer will want to review the candidate’s experience and abilities. In this meeting, the employer will
                            try to find out:
                            <br>
                            <ul>
                                <li>Can you do the job? (skills, abilities, qualifications)</li>
                                <li>Will you do the job? (interest, attitude & motivation)</li>
                                <li>How will you fit into the organisation? (personality)</li>
                            </ul>
                            <br>
                            As the job applicant, you might also have the objective to find out:
                            <br>
                            <ul>
                                <li>What does this position offer me?</li>
                                <li>How does it fit with my career plans?</li>
                            </ul>
                            <br>
                            Never go unprepared for a job interview! Preparation is key to success:
                            <br>
                            <ul>
                                <li>Review your own skills, experiences and qualities</li>
                                <li>Check your CV again</li>
                                <li>Anticipate questions and identify relevant examples</li>
                                <li>Prepare key selling points (self-marketing)</li>
                                <li>Research about the organisation or company</li>
                                <li>Check their websites, reports, articles, company literature etc.</li>
                                <li>Contacts with knowledge of organisation or sector can help</li>
                                <li>Read relevant articles in the press</li>
                                <li>Think about a personal visit or telephone call before the interview</li>
                                <li>Research job and occupational area</li>
                                <li>Job description – or similar</li>
                                <li>Current issues</li>
                                <li>What are salary standards for such positions or similar ones?</li>
                                <li>Prepare your questions</li>
                                <li>Practice, Practice, Practice!! You can ask your nearest employment service centre for help, or practice with family and
                                    friends.</li>

                            </ul>


