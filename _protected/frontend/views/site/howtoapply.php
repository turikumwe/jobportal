                        <h3>How to write a cover letter?</h3>
                        <p>
                            A cover letter is a letter sent alongside your CV and other attachments to introduce yourself and explain why you are
                            sending in the application. It also provides more information about yourself.
                            <br>
                            <br>
                            Make sure you cover letter is
                            <br>
                            <br>
                            <ol>
                                <li>
                                    <p>Designed for the specific job opening</p>
                                    <ul>
                                        <li>Adapt letter to needs of the job you want</li>
                                        <li>Refer to that job in opening sentences</li>
                                    </ul>
                                </li>
                                <li>
                                    <p>Describing your related skills</p>
                                    <ul>
                                        <li>Tie your experience to your skills</li>
                                        <li>Show how they directly relate to the job description</li>
                                    </ul>
                                </li>
                                <li>
                                    <p>Is personal</p>
                                    <ul>
                                        <li>Address the letter to a specific person/ employer (the receiver mentioned in the advert)</li>
                                        <li>If you don’t have a contact, use the organization’s contact</li>
                                        <li>Use a comma after the name (eg.: Dear Ms. Xx, Dear Mr Xx, or Dear Manager, …)</li>
                                    </ul>
                                </li>
                                <li>
                                    <p>Is interesting and convincing</p>
                                    <ul>
                                        <li>Keep in mind that readers go through the letter’s contents</li>
                                        <li>Open with attention-grabbing sentences</li>
                                        <li>Describe why you’re the best for the job</li>
                                    </ul>
                                </li>
                            </ol>
                            <br>
                            If you are registered at KORA you can save a cover letter example, but don’t forget you always have to re-write it and
                            adapt it to every vacancy! Never use the same letter twice!
                        </p>
