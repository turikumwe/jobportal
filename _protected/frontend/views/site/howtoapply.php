                        <h3> How to write a CV?</h3>
                        <p>
                            A CV (curriculum vitae) allows you to summarise your education, skills and experience and helps you market yourself to
                            employers. Typically, CVs are at least two or three pages.
                            When writing a CV, there are a few helpful rules you should follow;
                            <br>
                            <br>
                            <ul>
                                <li>
                                    <p>Match your CV to the position. Make sure that you highlight your education, work experience, and skills to relate to the
                                        particular industry or job. For example, if you are applying for a job in education, you might want to put your teaching
                                        experience at the top of your CV.</p>
                                </li>
                                <li>
                                    <p>You can also include keywords from the job description in your CV. This will show the employer that you are an ideal fit
                                        for the position.</p>
                                </li>
                                <li>
                                    <p>Use a template. You may want to use a template to structure your CV. This will give your application a clear structure,
                                        which will help the employer quickly see your qualifications and experience.</p>
                                </li>
                                <li>
                                    <p>You need to thoroughly edit your document to make sure there are no spelling or grammatical errors and that the format
                                        is uniform – for example, if you use bullet points in one job description, use the same bullet points in all the
                                        remaining job descriptions..</p>
                                </li>
                            </ul>
                        </p>
