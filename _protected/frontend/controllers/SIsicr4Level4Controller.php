    public function actionLists($id){
          
        $countLevel = SIsicr4Level4::find()
                    ->where('level3_id=:u',['u'=>$id])
                    ->count();

        $levels = SIsicr4Level4::find()
                   ->where('level3_id=:u',['u'=>$id])
                    ->all(); 


        if($countLevel > 0)
        {
            echo "<option></option>";
            foreach($levels as $level){
                echo "<option value='".$level->id."'>".$level->ecosector."</option>";     
             }
        }
        else{
            echo "<option>-</option>";
        }

        die;
